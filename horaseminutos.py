'''
Python nos fornece um segundo tipo de operador de divisão. Essa versão, chamada divisão inteira, usa o operador //.
Ela sempre trunca o resultado para o menor inteiro mais próximo.
'''

print(7 / 4)
print(7 // 4)
minutos = 645
horas = minutos // 60
print(horas)

### essa informação pode ser muito útil '//' ###

